def get_bmi(height, weight):
    height = 0
    weight = 0

    bmi = height * 2 // weight

    if bmi < 18.5:
        print("저체중")
    elif 18.5 <= bmi < 23:
        print("정상")
    elif 23 <= bmi < 25:
        print("과체중")
    elif 25 <= bmi:
        print("비만")

    return height,weight

